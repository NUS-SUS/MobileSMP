package com.example.mobilesmp.ui.profile;

public class TwoStrings {
    private String left;
    private String right;

    public TwoStrings(String left, String right) {
        this.left = left;
        this.right = right;
    }

    public String getLeft() {
        return this.left;
    }

    public String getRight() {
        return this.right;
    }

}
